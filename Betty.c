#include <stdio.h>

/**
 * print_hello - Prints a greeting message
 *
 * Description: This function prints a greeting message to the standard output.
 * Return: None
 */
void print_hello(void)
{
printf("Hello, Betty!\n");
}

/**
 * main - Entry point of the program
 *
 * Description: This is the main function that serves as the entry point of
 * the program. It calls the print_hello function.
 * Return: 0 on success
 */
int main(void)
{
print_hello();
return 0;
}

